// ===== Quiz Question Generator =====
function generateQuizQuestions(scenario) {
  const m = scenario.modules;
  const questions = [];

  // Q1: Decision maker identification
  const allDM = SCENARIOS.flatMap(s => s.modules.targetCustomers.decisionMakers);
  const wrongDM = allDM.filter(d => !m.targetCustomers.decisionMakers.includes(d));
  questions.push({
    id: `${scenario.id}-q1`,
    scenarioId: scenario.id,
    question: `在「${scenario.name}」场景中，以下哪个是最典型的关键决策人？`,
    options: shuffle([
      { text: m.targetCustomers.decisionMakers[0], correct: true },
      ...wrongDM.slice(0, 3).map(t => ({ text: t, correct: false }))
    ]),
    explanation: `「${scenario.name}」场景的关键决策人通常包括：${m.targetCustomers.decisionMakers.join("、")}。`
  });

  // Q2: Core pain point
  const allPains = SCENARIOS.filter(s => s.id !== scenario.id).map(s => s.modules.businessConcerns[1] || s.modules.businessConcerns[0]);
  questions.push({
    id: `${scenario.id}-q2`,
    scenarioId: scenario.id,
    question: `以下哪项是「${scenario.name}」客户最典型的业务痛点？`,
    options: shuffle([
      { text: m.businessConcerns[0].split("：")[0] + "：" + (m.businessConcerns[0].split("：")[1] || m.businessConcerns[0]), correct: true },
      ...allPains.slice(0, 3).map(t => ({ text: t.split("：")[0] + "：" + (t.split("：")[1] || t), correct: false }))
    ]),
    explanation: `「${scenario.name}」核心痛点：${m.businessConcerns[0]}`
  });

  // Q3: Pilot approach
  const allPilots = SCENARIOS.filter(s => s.id !== scenario.id).map(s => s.modules.pilotSuggestions[0]);
  questions.push({
    id: `${scenario.id}-q3`,
    scenarioId: scenario.id,
    question: `「${scenario.name}」场景，最推荐的第一步试点切入方式是？`,
    options: shuffle([
      { text: m.pilotSuggestions[0], correct: true },
      ...allPilots.slice(0, 3).map(t => ({ text: t, correct: false }))
    ]),
    explanation: `「${scenario.name}」推荐试点切入方式：${m.pilotSuggestions[0]}`
  });

  // Q4: Key opening question
  questions.push({
    id: `${scenario.id}-q4`,
    scenarioId: scenario.id,
    question: `拜访「${scenario.name}」客户，首先应该确认哪方面的现状？`,
    options: shuffle([
      { text: m.keyQuestions[0], correct: true },
      ...SCENARIOS.filter(s => s.id !== scenario.id)
        .map(s => s.modules.keyQuestions[0])
        .slice(0, 3)
        .map(t => ({ text: t, correct: false }))
    ]),
    explanation: `拜访「${scenario.name}」首要了解：${m.keyQuestions[0]}`
  });

  return questions;
}

function shuffle(arr) {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

